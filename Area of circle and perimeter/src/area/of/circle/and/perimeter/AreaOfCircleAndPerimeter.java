package area.of.circle.and.perimeter;

/**
 *
 * @author Md. Nazmul
 */
public class AreaOfCircleAndPerimeter {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        double f=10;
        double pi=3.14;
        double A=pi*f*f;
        System.out.println("Area is "+A);
        double p=12;
        double P=2*pi*p;
        System.out.println("Perimeter is "+P);
    }
    
}
